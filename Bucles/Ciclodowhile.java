/*
 *  Ciclo do While
 * 
 *  do{
 *      Instrucciones
 *  } while(condicion);
 */
package Bucles;

public class Ciclodowhile {
    
    public static void main(String [] args){

        int i = 1;

        do{
            System.out.println(i);
            i++;
            
        }while(i<=10);
    }
}
